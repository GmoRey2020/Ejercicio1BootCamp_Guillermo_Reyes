package Ejercicios;

public class ValorAbsoluto {
    public static void main(String[] args) {
        // Declaración e inicialización de la variable
        int a = -15;

        // Calcular el valor absoluto usando Math.abs()
        int valorAbsoluto = Math.abs(a);
        System.out.println("El valor absoluto de " + a + " es: " + valorAbsoluto);
    }
}
