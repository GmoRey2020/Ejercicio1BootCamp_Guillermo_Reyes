package Ejercicios;

public class ConversionTemperatura {
    public static void main(String[] args) {
        // Declaración e inicialización de la variable
        double c = 25; // temperatura en grados Celsius
        // Convertir la temperatura de Celsius a Fahrenheit
        double fahrenheit = (c * 9 / 5) + 32;
        System.out.println(c + " grados Celsius son " + fahrenheit + " grados Fahrenheit.");
    }
}
